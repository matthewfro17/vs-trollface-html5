function onCreate()
	for i = 0, getProperty('unspawnNotes.length')-1 do
		if getPropertyFromGroup('unspawnNotes', i, 'noteType') == 'Bob Note' then
			setPropertyFromGroup('unspawnNotes', i, 'texture', 'bob_notes');
			setPropertyFromGroup('unspawnNotes', i, 'hitHealth', '0.023');
			setPropertyFromGroup('unspawnNotes', i, 'missHealth', '0.0475');
			setPropertyFromGroup('unspawnNotes', i, 'hitCausesMiss', false);

			if getPropertyFromGroup('unspawnNotes', i, 'mustPress') then
				setPropertyFromGroup('unspawnNotes', i, 'ignoreNote', false);
			end
		end
	end
end

function noteMiss(id, noteData, noteType, isSustainNote)
	if noteType == 'Bob Note' then
		uhoh = getRandomInt(1, 100)
		if downscroll == true then
			triggerEvent('Camera Flash', 'ffffff', '0.5')
			if uhoh < 11 then
				swap1()
			elseif uhoh > 10 and uhoh < 21 then
				swap2()
			elseif uhoh > 20 and uhoh < 31 then
				swap3()
			elseif uhoh > 30 and uhoh < 41 then
				swap4()
			elseif uhoh > 40 and uhoh < 51 then
				swap5()
			elseif uhoh > 50 and uhoh < 61 then
				swap6()
			elseif uhoh > 60 and uhoh < 71 then
				swap7()
			elseif uhoh > 70 and uhoh < 81 then
				swap8()
			elseif uhoh > 80 and uhoh < 91 then
				swap9()
			elseif uhoh > 90 then
				swap0()
			end
			noteTweenAngle("Note0angle", 0, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note1angle", 1, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note2angle", 2, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note3angle", 3, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note4angle", 4, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note5angle", 5, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note6angle", 6, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note7angle", 7, getRandomInt(-180, 180), 0.001, 'linear')
		else
			triggerEvent('Camera Flash', 'ffffff', '0.5')
			if uhoh < 11 then
				swap1up()
			elseif uhoh > 10 and uhoh < 21 then
				swap2up()
			elseif uhoh > 20 and uhoh < 31 then
				swap3up()
			elseif uhoh > 30 and uhoh < 41 then
				swap4up()
			elseif uhoh > 40 and uhoh < 51 then
				swap5up()
			elseif uhoh > 50 and uhoh < 61 then
				swap6up()
			elseif uhoh > 60 and uhoh < 71 then
				swap7up()
			elseif uhoh > 70 and uhoh < 81 then
				swap8up()
			elseif uhoh > 80 and uhoh < 91 then
				swap9up()
			elseif uhoh > 90 then
				swap0up()
			end
			noteTweenAngle("Note0angle", 0, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note1angle", 1, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note2angle", 2, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note3angle", 3, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note4angle", 4, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note5angle", 5, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note6angle", 6, getRandomInt(-180, 180), 0.001, 'linear')
			noteTweenAngle("Note7angle", 7, getRandomInt(-180, 180), 0.001, 'linear')
		end
	end
end

